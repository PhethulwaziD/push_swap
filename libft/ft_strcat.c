#include "libft.h"

char		*ft_strcat(char *dst, const char *src)
{
	int i;
	int n;

	i = 0;
	n = 0;
	i = ft_strlen(dst);
	while (src[n])
	{
		dst[i] = src[n];
		n++;
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
